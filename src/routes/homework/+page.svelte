                    <p>Действие романа разворачивается не на родном американском Юге, который станет местом действия второго романа Тартт «Маленький друг», а в Новой Англии, в колледже Хэмпден, во многих отношениях напоминающем Беннингтон. Основанный на матрице университетского романа, он повествует о тесно сплоченной группе из шести студентов, изучающих древнегреческий язык.</p>
                    <p>Первоначально роман назывался «Бог иллюзий» (на некоторых языках до сих пор так), о чём сама писательница говорит в интервью. Такое название имело основание, поскольку роман исследует, как именно влияют университетское образование и художественные тексты на формирование личности. Название «Тайная история» отсылает к произведению византийского историка Прокопия Кесарийского. Исполняя обязанности военного летописца при дворе императора Юстиниана, он написал восьмитомную «Историю войн», которую снабдил «неофициальным» дополнением — памфлетом под заглавием «Anecdota». В нем он излагает закулисные причины описанных в «Истории» политических событий и весьма критически оценивает действия императора. Греческое название памфлета переводилось на английский как «The Secret History», а на русский — как «Тайная история».</p>
